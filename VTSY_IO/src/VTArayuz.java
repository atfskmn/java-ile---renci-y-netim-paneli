    private void tblKayitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKayitMouseClicked
        if (tblKayit.getSelectedRow()!=-1) {
            String ogrenci_No=tblKayit.getValueAt(tblKayit.getSelectedRow(),0).toString();
            MyConnector connection = new MyConnector();
           Connection conn = connection.getConnector();
           
           
            try {
                Statement statement=conn.createStatement();
               
                ResultSet rs = statement.executeQuery("SELECT * FROM durum WHERE ogrenci_No='" + ogrenci_No + "'");
               rs.next();
               txtNumara.setText(rs.getString("ogrenci_No"));
               txtAd.setText(rs.getString("ogrenci_Ad"));
               txtSoyad.setText(rs.getString("ogrenci_soyad"));
               jcCinsiyet.setSelectedIndex(rs.getInt("cinsiyet"));
              String tarihStr = rs.getString("dogum_tarihi");
                if (tarihStr != null && !tarihStr.isEmpty()) {
                    try {
                        // Veritabanýndaki formatý belirleyerek doðru þekilde parse edelim
                        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                        Date dogumTarihi = sdf.parse(tarihStr);
                        spnDtarih.setValue(dogumTarihi);
                    } catch (ParseException e) {
                        JOptionPane.showMessageDialog(rootPane, "Tarih formatý hatalý: " + tarihStr);
                        e.printStackTrace();
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Doðum tarihi verisi boþ!");
                }
               
                if (rs.getInt("stajDurumu")==1) {
                    jCStaj.setSelected(true);
                    
                }else{
                
                jCStaj.setSelected(false);
                }
               
                
            } catch (SQLException sqlExp) {
                    JOptionPane.showMessageDialog(rootPane,"Kayýt Getirilme Baþarýlý");
                    sqlExp.printStackTrace();
            }finally{
            connection.closeConnector();
            }
           
           
           listele();
        }
        
        
        
        
        
    }//GEN-LAST:event_tblKayitMouseClicked

