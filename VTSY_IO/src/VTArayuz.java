    public void listele() {
    MyConnector connection = new MyConnector();
    Connection conn = connection.getConnector();

    DefaultTableModel dtm = (DefaultTableModel) tblKayit.getModel();
    
    // Önce tabloyu temizleyelim, yoksa tekrar tekrar veri eklenir
    dtm.setRowCount(0);

    try {
        Statement statement = conn.createStatement();
        ResultSet rs = statement.executeQuery("SELECT * FROM durum");

        while (rs.next()) {
            String ogrenciNo = rs.getString("ogrenci_No").trim(); 
            String ad = rs.getString("ogrenci_Ad");
            String soyad = rs.getString("ogrenci_soyad");
            String dogumTarihi = rs.getString("Dogum_tarihi");
            String stajDurumu = rs.getString("stajDurumu");

            // Cinsiyet sütununu al ve kontrol et
            String cinsiyetStr = rs.getString("cinsiyet").trim(); 
            String cinsiyet;

            if (cinsiyetStr.equals("0")) {
                cinsiyet = "Erkek";
            } else if (cinsiyetStr.equals("1")) {
                cinsiyet = "Kadýn";
            } else {
                cinsiyet = "Bilinmiyor"; // Beklenmeyen bir deðer varsa
            }

            // Veriyi tabloya ekle
            dtm.addRow(new String[]{ogrenciNo, ad, soyad, cinsiyet, dogumTarihi, stajDurumu});
        }

    } catch (SQLException sqlExp) {
        JOptionPane.showMessageDialog(rootPane, "Kayýtlar getirilemedi");
        sqlExp.printStackTrace(); // Hata detaylarýný görmek için
    } finally {
        connection.closeConnector();
    }
}

    
    

